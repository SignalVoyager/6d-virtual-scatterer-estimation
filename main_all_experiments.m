% MAIN_ALL_EXPERIMENTS - Entry point for running multiple experiments with different RNG seeds
%
% SYNTAX:
%   main_all_experiments()
%
% DESCRIPTION:
%   This function orchestrates the execution of experiments organized in a standardized
%   directory structure. Each experiment is located in project/experiments/<exp_name>/
%   and must contain:
%     - config.json: experiment configuration parameters
%     - run_experiment.m: experiment execution script
%     - data/: directory for intermediate artifacts
%     - outputs/: directory for figures, logs, and metrics
%
% WORKFLOW:
%   1. Determines the project root directory (location of this script)
%   2. Adds source directories to MATLAB path
%   3. Iterates through specified experiments
%   4. For each experiment and RNG seed:
%      - Validates experiment structure (config.json, run_experiment.m exist)
%      - Clears runtime state and resets random number generator
%      - Executes the experiment-local script with expRoot injected into workspace
%
% INPUTS:
%   None (configuration is hard-coded within the function)
%
% OUTPUTS:
%   None (outputs are generated by individual experiment scripts)
%
% NOTES:
%   - expNames: cell array of experiment folder names to execute
%   - rngValues: vector of RNG seeds for reproducibility; each experiment runs
%     once per seed value
%   - The function clears functions between runs to reset Python adapter state
%
% SEE ALSO:
%   addpath, genpath, run, rng, isfolder, isfile
function main_all_experiments()
    clc; close all;

    % ---------- project root (this file's folder) ----------
    projectRoot = fileparts(mfilename('fullpath'));

    % ---------- add source paths ----------
    addpath(fullfile(projectRoot, "src"));
    addpath(genpath(fullfile(projectRoot, "src")));

    % ---------- choose experiments ----------
    expNames = ["exp05_sector_number_sensitivity"]; % experiment folders under project/experiments/
    % expNames = ["exp03_model_comparison"];

    % ---------- choose RNG seeds ----------
    % rngValues = [521,75,185,19,424,7172,1,2354,888,48];  % can be vector
    rngValues = [521,75,185,19,424];

    for e = 1:numel(expNames)
        expName = expNames(e);
        expRoot = fullfile(projectRoot, "experiments", expName);
        cfgPath = fullfile(expRoot, "config.json");
        logDir = fullfile(expRoot, "outputs", "logs");
        logFile = fullfile(logDir, sprintf("%s.log", expName));
        runScript = fullfile(expRoot, "run_experiment.m");

        % minimal sanity
        assert(isfolder(expRoot), "Experiment folder not found: %s", expRoot);
        assert(isfile(cfgPath), "Missing config.json in %s", expRoot);
        assert(isfile(runScript), "Missing run_experiment.m in %s", expRoot);

        cfg = jsondecode(fileread(cfgPath));
        if isstruct(cfg.dataSetList), cfg.dataSetList = num2cell(cfg.dataSetList); end
        utils.validateExperimentConfig(cfg);
        outputMode = lower(string(cfg.runtime.outputMode));
        doLog = (outputMode == "log");
        if doLog
            assert(isfolder(logDir), "Missing log directory: %s", logDir);
            if isfile(logFile), delete(logFile); end
        end

        for s = 1:numel(rngValues)
            seed = rngValues(s);

            fprintf("\n================ EXP: %s | SEED: %d ================\n", expName, seed);
            if doLog
                fprintf("Log file: %s\n", logFile);
            else
                fprintf("Output mode: console (no log file)\n");
            end

            % ---------- reset runtime state ----------
            clc;
            close all;
            rng(seed, "twister");

            % (optional) clear python adapter persistent state if needed
            clear functions; %#ok<CLFUNC>
            % If your sionna adapter uses persistent variables inside methods,
            % you may also want:
            % clear classes;

            % ---------- run experiment-local script + full logging ----------
            tSeed = tic;
            seedStart = char(datetime("now", "Format", "yyyy-MM-dd HH:mm:ss"));
            runScriptEsc = strrep(char(runScript), '''', '''''');
            runCmd = sprintf('run(''%s'');', runScriptEsc);

            try
                if outputMode == "console"
                    % Console-only mode: keep real-time output.
                    run(runScript);
                else
                    % Log mode: capture output for log writing.
                    runOutput = evalc(runCmd);
                    utils.appendSeedLog(logFile, expName, seed, seedStart, ...
                        sprintf("[END]   %s (%.2fs)\n", ...
                        char(datetime("now", "Format", "yyyy-MM-dd HH:mm:ss")), toc(tSeed)), ...
                        string(runOutput));
                end
            catch ME
                if doLog
                    utils.appendSeedLog(logFile, expName, seed, seedStart, ...
                        sprintf("[FAILED] %s (%.2fs)\n", ...
                        char(datetime("now", "Format", "yyyy-MM-dd HH:mm:ss")), toc(tSeed)), ...
                        string(getReport(ME, "extended", "hyperlinks", "off")));
                end
                rethrow(ME);
            end
        end
    end
end

